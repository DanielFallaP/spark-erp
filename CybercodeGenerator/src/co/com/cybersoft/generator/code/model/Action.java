package co.com.cybersoft.generator.code.model;

public class Action {
	private String className;
	private String method;
	private String actionName;
	private String spanishActionName;
	
	public String getSpanishActionName() {
		return spanishActionName;
	}
	public void setSpanishActionName(String spanishActionName) {
		this.spanishActionName = spanishActionName;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	public String getActionName() {
		return actionName;
	}
	public void setActionName(String actionName) {
		this.actionName = actionName;
	}
	
	
}
